public class LiteratureReviewException extends Exception {

    public LiteratureReviewException() { super(); }
    public LiteratureReviewException(String message) { super(message); }
    public LiteratureReviewException(String message, Throwable cause) { super(message, cause); }
    public LiteratureReviewException(Throwable cause) { super(cause); }

}
